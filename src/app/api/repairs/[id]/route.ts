import { NextRequest, NextResponse } from 'next/server';
import { connectDB } from '@/lib/db';
import Repair from '@/models/Repair';
import Car from '@/models/Car';
import User from '@/models/User';
import { getAuthPayload } from '@/lib/apiAuth';
import { logActivity } from '@/lib/activityLogger';

import mongoose from 'mongoose';

async function updateCarRepairCost(carObjectId: string) {
  const result = await Repair.aggregate([
    { $match: { car: new mongoose.Types.ObjectId(carObjectId), isDeleted: { $ne: true } } },
    { $group: { _id: null, total: { $sum: '$totalCost' } } },
  ]);

  const pendingRepairs = await Repair.countDocuments({
    car: new mongoose.Types.ObjectId(carObjectId),
    status: { $in: ['Pending', 'In Progress'] },
    isDeleted: { $ne: true }
  });

  const car = await Car.findById(carObjectId);
  let newStatus = car?.status;
  
  if (pendingRepairs > 0 && car?.status === 'In Stock') {
    newStatus = 'Under Repair';
  } else if (pendingRepairs === 0 && car?.status === 'Under Repair') {
    newStatus = 'In Stock';
  }

  await Car.findByIdAndUpdate(carObjectId, { 
    totalRepairCost: result[0]?.total || 0,
    status: newStatus
  });
}

export async function GET(
  _request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const auth = await getAuthPayload(_request);
    if (!auth) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });

    if (!['Admin', 'Car Manager', 'Accountant', 'Finance Manager'].includes(auth.normalizedRole || '')) {
      return NextResponse.json({ error: 'Forbidden' }, { status: 403 });
    }

    await connectDB();
    const { id } = await params;
    const repair = await Repair.findById(id)
      .populate('car', 'carId brand model year plateNumber chassisNumber engineNumber sequenceNumber color')
      .populate('createdBy', 'name')
      .lean();
    if (!repair) return NextResponse.json({ error: 'Repair not found' }, { status: 404 });
    return NextResponse.json({ repair });
  } catch (error) {
    console.error('Get repair error:', error);
    return NextResponse.json({ error: 'Internal server error' }, { status: 500 });
  }
}

export async function PUT(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const auth = await getAuthPayload(request);
    if (!auth) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });

    if (!['Admin', 'Car Manager'].includes(auth.normalizedRole || '')) {
      return NextResponse.json({ error: 'Forbidden' }, { status: 403 });
    }

    await connectDB();
    const { id } = await params;
    const body = await request.json();

    const repair = await Repair.findByIdAndUpdate(id, body, { new: true, runValidators: true });
    if (!repair) return NextResponse.json({ error: 'Repair not found' }, { status: 404 });

    // Update related transaction if cost changed
    if (body.totalCost !== undefined) {
      const Transaction = (await import('@/models/Transaction')).default;
      await Transaction.findOneAndUpdate(
        { referenceId: id, referenceType: 'Repair', isAutoGenerated: true },
        { amount: repair.totalCost }
      );
    }

    await updateCarRepairCost(repair.car.toString());

    await logActivity({
      userId: auth.userId,
      userName: auth.name,
      action: `Updated repair for car ${repair.carId}`,
      module: 'Repairs',
      targetId: repair._id.toString(),
      ipAddress: request.headers.get('x-forwarded-for') || 'unknown',
    });

    return NextResponse.json({ repair });
  } catch (error) {
    console.error('Update repair error:', error);
    return NextResponse.json({ error: 'Internal server error' }, { status: 500 });
  }
}

export async function DELETE(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const auth = await getAuthPayload(request);
    if (!auth) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });

    if (!['Admin', 'Car Manager'].includes(auth.normalizedRole || '')) {
      return NextResponse.json({ error: 'Forbidden' }, { status: 403 });
    }

    await connectDB();
    const { id } = await params;
    const repair = await Repair.findByIdAndUpdate(
      id,
      { isDeleted: true },
      { new: true }
    );
    if (!repair) return NextResponse.json({ error: 'Repair not found' }, { status: 404 });

    // Soft delete associated transaction
    const Transaction = (await import('@/models/Transaction')).default;
    await Transaction.findOneAndUpdate(
      { referenceId: id, referenceType: 'Repair', isAutoGenerated: true },
      { isDeleted: true }
    );

    await updateCarRepairCost(repair.car.toString());

    await logActivity({
      userId: auth.userId,
      userName: auth.name,
      action: `Deleted repair for car ${repair.carId}`,
      module: 'Repairs',
      targetId: id,
      ipAddress: request.headers.get('x-forwarded-for') || 'unknown',
    });

    return NextResponse.json({ message: 'Repair deleted successfully' });
  } catch (error) {
    console.error('Delete repair error:', error);
    return NextResponse.json({ error: 'Internal server error' }, { status: 500 });
  }
}
