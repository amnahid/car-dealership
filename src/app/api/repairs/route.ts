import { NextRequest, NextResponse } from 'next/server';
import { connectDB, DatabaseConnectionError } from '@/lib/db';
import Repair from '@/models/Repair';
import Car from '@/models/Car';
import Transaction from '@/models/Transaction';
import User from '@/models/User';
import { getAuthPayload } from '@/lib/apiAuth';
import { logActivity } from '@/lib/activityLogger';

import mongoose from 'mongoose';

async function updateCarRepairCost(carId: string) {
  const result = await Repair.aggregate([
    { $match: { car: new mongoose.Types.ObjectId(carId) } },
    { $group: { _id: null, total: { $sum: '$totalCost' } } },
  ]);
  await Car.findByIdAndUpdate(carId, { totalRepairCost: result[0]?.total || 0 });
}

export async function GET(request: NextRequest) {
  try {
    const auth = await getAuthPayload(request);
    if (!auth) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });

    if (!['Admin', 'Car Manager', 'Accountant', 'Finance Manager'].includes(auth.normalizedRole || '')) {
      return NextResponse.json({ error: 'Forbidden' }, { status: 403 });
    }

    await connectDB();
    const { searchParams } = new URL(request.url);
    const carId = searchParams.get('carId');
    const page = parseInt(searchParams.get('page') || '1');
    const limit = parseInt(searchParams.get('limit') || '20');

    const query: Record<string, unknown> = {
      isDeleted: { $ne: true },
    };
    if (carId) query.car = carId;

    const [total, repairs] = await Promise.all([
      Repair.countDocuments(query),
      Repair.find(query)
        .sort({ repairDate: -1 })
        .skip((page - 1) * limit)
        .limit(limit)
        .populate('car', 'carId brand model')
        .populate('createdBy', 'name')
        .lean(),
    ]);

    return NextResponse.json({
      repairs,
      pagination: { total, page, limit, pages: Math.ceil(total / limit) },
    });
  } catch (error) {
    console.error('Get repairs error:', error);

    if (error instanceof DatabaseConnectionError) {
      return NextResponse.json({ error: error.message }, { status: error.statusCode });
    }

    return NextResponse.json({ error: 'Internal server error' }, { status: 500 });
  }
}

export async function POST(request: NextRequest) {
  try {
    const auth = await getAuthPayload(request);
    if (!auth) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });

    if (!['Admin', 'Car Manager'].includes(auth.normalizedRole || '')) {
      return NextResponse.json({ error: 'Forbidden' }, { status: 403 });
    }

    await connectDB();
    const body = await request.json();
    const repair = await Repair.create({ ...body, createdBy: auth.userId });

    const transactionPromise = repair.totalCost && repair.totalCost > 0
      ? Transaction.create({
          date: new Date(repair.repairDate),
          type: 'Expense',
          category: 'Car Repair',
          amount: repair.totalCost,
          description: `Repair for car ${body.carId} - ${repair.repairDescription || 'Maintenance'}`,
          referenceId: repair._id.toString(),
          referenceType: 'Repair',
          isAutoGenerated: true,
          createdBy: auth.userId,
        })
      : Promise.resolve(null);

    await Promise.all([updateCarRepairCost(body.car), transactionPromise]);

    await logActivity({
      userId: auth.userId,
      userName: auth.name,
      action: `Created repair for car ${body.carId}`,
      module: 'Repairs',
      targetId: repair._id.toString(),
      ipAddress: request.headers.get('x-forwarded-for') || 'unknown',
    });

    return NextResponse.json({ repair }, { status: 201 });
  } catch (error) {
    console.error('Create repair error:', error);

    if (error instanceof DatabaseConnectionError) {
      return NextResponse.json({ error: error.message }, { status: error.statusCode });
    }

    return NextResponse.json({ error: 'Internal server error' }, { status: 500 });
  }
}
